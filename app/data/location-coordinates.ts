const coordinatesBySlug = new Map(
  locationCoordinates.map((coordinate) => [coordinate.slug, coordinate]),
);

function normalizeLocationSlug(value?: string) {
  if (!value) {
    return "";
  }

  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function getLocationCoordinate(...locations: (string | undefined)[]) {
  for (const location of locations) {
    const slug = normalizeLocationSlug(location);

    if (!slug) {
      continue;
    }

    const coordinate = coordinatesBySlug.get(slug);

    if (coordinate) {
      return {
        latitude: coordinate.latitude,
        longitude: coordinate.longitude,
        source: coordinate.slug,
      };
    }
  }

  return null;
}
